import type { Question } from '../../types/question';
import type {
  ShapePattern,
  OverlayShapeQuestionData,
  OverlayShapeChoiceData,
} from './types';

// ─── 固定問題プール（画像の4問） ───
// 座標は 0〜100 の正規化座標。左上が (0,0)、右下が (100,100)

interface FixedQ {
  sheetA: ShapePattern;
  sheetB: ShapePattern;
  /** 正解（2枚を重ねた結果） */
  answer: ShapePattern;
  /** 不正解の選択肢（3つ） */
  distractors: ShapePattern[];
}

/**
 * 問題1
 * 左シート: 右上→左下の対角線で分割、右上が黒
 *   三角形: (100,0) (100,100) (0,100)  ← 左下三角が白なので、右上三角が黒
 *   黒領域: (0,0) (100,0) (100,100) の三角形
 * 右シート: 左上→右下の対角線で分割、左上が黒
 *   黒領域: (0,0) (100,0) (0,100) の三角形
 * 重ね結果: 両方のOR → 中央に小さな白い三角が2つ残る
 *   → (0,0)(100,0)(100,100) ∪ (0,0)(100,0)(0,100)
 *   = 全体から右下の小三角を除いた形... 実際は (0,0)(100,0)が共通辺
 *   左: 対角線(0,0)→(100,100)の上側が黒
 *   右: 対角線(100,0)→(0,100)の左側が黒
 *   OR: ほぼ全面黒、中央下に白い三角が残る
 *
 * 画像を再確認:
 * 左シート: 対角線が左上(0,0)→右下(100,100)、上三角(右上側)が黒
 *   黒: (0,0)(100,0)(100,100)
 * 右シート: 対角線が右上(100,0)→左下(0,100)、上三角(左上側)が黒
 *   黒: (0,0)(100,0)(0,100)
 * OR結果: (0,0)(100,0)(100,100) + (0,0)(100,0)(0,100)
 *   → 上半分は完全に黒、下半分は左三角と右三角で、中央下に白い菱形が残る
 *   実際には白い部分は (0,100)(50,50)(100,100) の三角形のみ... ではなく
 *   左の黒は (0,0)(100,0)(100,100) → 右下三角
 *   右の黒は (0,0)(100,0)(0,100) → 左上三角
 *   合わせると白い部分は無い？ いや、
 *   左の白: (0,0)(0,100)(100,100) の左下三角
 *   右の白: (100,0)(100,100)(0,100) の右下三角
 *   両方白 = 左下三角 ∩ 右下三角 = (50,50)(0,100)(100,100) の下中央三角
 *   → 正解: 全面黒で、下中央に白い逆三角形
 */
const q1: FixedQ = {
  // 左: 対角線(0,0)→(100,100)の右上が黒
  sheetA: ['0,0 100,0 100,100'],
  // 右: 対角線(100,0)→(0,100)の左上が黒
  sheetB: ['0,0 100,0 0,100'],
  // 正解: 全面から下中央の三角を除く → 2つのポリゴンで表現
  answer: ['0,0 100,0 100,100', '0,0 100,0 0,100'],
  distractors: [
    // 不正解1: 全面黒
    ['0,0 100,0 100,100 0,100'],
    // 不正解2: 上半分だけ黒
    ['0,0 100,0 100,50 0,50'],
    // 不正解3: 左の黒だけ
    ['0,0 100,0 100,100'],
  ],
};

/**
 * 問題2
 * 左シート: 十字型の黒（上半分左 + 下半分右）
 *   上左: (0,0)(50,0)(50,50)(0,50)
 *   下右: (50,50)(100,50)(100,100)(50,100)
 * 右シート: 対角線(0,0)→(100,100)の右下が黒
 *   黒: (0,100)(100,0)(100,100) → いや (100,0)(100,100)(0,100)
 *
 * 画像再確認:
 * 左シート: 上半分の左側が黒 + 下半分の右側が黒（チェッカーボード的）
 *   黒1: (0,0)(50,0)(50,50)(0,50)  上左
 *   黒2: (50,50)(100,50)(100,100)(50,100)  下右
 * 右シート: 対角線(100,0)→(0,100)で分割、右下が黒
 *   黒: (100,0)(100,100)(0,100)
 * OR結果: 上左の四角 + 下右の四角 + 右下三角
 */
const q2: FixedQ = {
  sheetA: [
    '0,0 50,0 50,50 0,50',
    '50,50 100,50 100,100 50,100',
  ],
  sheetB: ['100,0 100,100 0,100'],
  answer: [
    '0,0 50,0 50,50 0,50',
    '50,50 100,50 100,100 50,100',
    '100,0 100,100 0,100',
  ],
  distractors: [
    // 不正解1: 左シートだけ
    ['0,0 50,0 50,50 0,50', '50,50 100,50 100,100 50,100'],
    // 不正解2: 右シートだけ
    ['100,0 100,100 0,100'],
    // 不正解3: 全面黒
    ['0,0 100,0 100,100 0,100'],
  ],
};

/**
 * 問題3
 * 左シート: 右向き矢印の白抜き（背景黒、中央に右向き三角の白）
 *   黒: 全面から右向き三角を除く
 *   右向き三角(白): (0,20)(80,50)(0,80) くらい
 *   → 黒を表現: 上の台形 + 下の台形 + 右の三角
 *   簡略化: 全面黒の上に白三角を重ねる代わりに、黒領域をポリゴンで定義
 *   黒: (0,0)(100,0)(100,100)(0,100) から (0,25)(75,50)(0,75) を除く
 *   → 複雑なので、黒領域を複数ポリゴンで:
 *     上部: (0,0)(100,0)(100,50)(75,50)(0,25)
 *     下部: (0,75)(75,50)(100,50)(100,100)(0,100)
 *
 * 右シート: 左向き矢印の白抜き（背景黒、中央に左向き三角の白）
 *   左向き三角(白): (100,25)(25,50)(100,75)
 *   黒:
 *     上部: (0,0)(100,0)(100,25)(25,50)(0,50) → (0,0)(100,0)(100,25)(25,50)(0,0)
 *     下部: (0,50)(25,50)(100,75)(100,100)(0,100)
 *
 * OR結果: ほぼ全面黒、中央に小さな白い菱形
 */
const q3: FixedQ = {
  sheetA: [
    '0,0 100,0 100,50 75,50 0,25',
    '0,75 75,50 100,50 100,100 0,100',
  ],
  sheetB: [
    '0,0 100,0 100,25 25,50 0,0',
    '0,100 25,50 100,75 100,100',
  ],
  answer: [
    '0,0 100,0 100,50 75,50 0,25',
    '0,75 75,50 100,50 100,100 0,100',
    '0,0 100,0 100,25 25,50 0,0',
    '0,100 25,50 100,75 100,100',
  ],
  distractors: [
    // 不正解1: 全面黒
    ['0,0 100,0 100,100 0,100'],
    // 不正解2: 左シートだけ
    ['0,0 100,0 100,50 75,50 0,25', '0,75 75,50 100,50 100,100 0,100'],
    // 不正解3: X字型の白抜き
    ['0,0 50,0 50,40 0,0', '50,0 100,0 100,40 50,0', '0,60 50,100 0,100', '100,60 50,100 100,100'],
  ],
};

/**
 * 問題4
 * 左シート: 蝶ネクタイ型（左上三角 + 右下三角が黒）
 *   左上三角: (0,0)(50,0)(0,50) → いや (0,0)(100,0)(0,100) の左上三角
 *   右下三角: (100,0)(100,100)(0,100) → いや
 *
 * 画像再確認:
 * 左シート: 左上に大きな三角が黒 + 右下に大きな三角が黒（砂時計/蝶ネクタイ型）
 *   黒1: (0,0)(50,50)(100,0) 上三角
 *   黒2: (0,100)(50,50)(100,100) 下三角
 * 右シート: 上部中央に小さな三角が黒
 *   黒: (25,0)(75,0)(50,50) くらい → いや画像では上部に三角
 *   → (0,0)(100,0)(50,50) 上向き三角
 *
 * OR結果: 上三角 + 下三角 + 上向き三角 → ほぼ全面
 */
const q4: FixedQ = {
  // 左: 蝶ネクタイ型（上三角+下三角）
  sheetA: [
    '0,0 100,0 50,50',
    '0,100 100,100 50,50',
  ],
  // 右: 上部に逆三角が黒
  sheetB: ['0,0 100,0 50,50'],
  answer: [
    '0,0 100,0 50,50',
    '0,100 100,100 50,50',
  ],
  distractors: [
    // 不正解1: 上三角だけ
    ['0,0 100,0 50,50'],
    // 不正解2: 全面黒
    ['0,0 100,0 100,100 0,100'],
    // 不正解3: 下三角だけ
    ['0,100 100,100 50,50'],
  ],
};

const FIXED_QUESTIONS: FixedQ[] = [q1, q2, q3, q4];

// ─── 問題生成 ───

const INSTRUCTION_TEXT =
  'とうめいなシートにかかれた2つのかたちを\nピッタリかさねたら、どんなもようになりますか？';

/** 固定問題プールからランダムに1問を生成する */
export function generateOverlayShapeQuestion(): Question<
  OverlayShapeQuestionData,
  OverlayShapeChoiceData
> {
  const fixedQ = FIXED_QUESTIONS[Math.floor(Math.random() * FIXED_QUESTIONS.length)];

  const correctIndex = Math.floor(Math.random() * 4);
  const choices: OverlayShapeChoiceData[] = fixedQ.distractors.map((d) => ({ pattern: d }));
  choices.splice(correctIndex, 0, { pattern: fixedQ.answer });

  return {
    questionData: {
      sheetA: fixedQ.sheetA,
      sheetB: fixedQ.sheetB,
    },
    choices,
    correctIndex,
    instructionText: INSTRUCTION_TEXT,
  };
}

/** 固定問題プールの全問題を返す */
export function getAllOverlayShapeQuestions(): Question<
  OverlayShapeQuestionData,
  OverlayShapeChoiceData
>[] {
  return FIXED_QUESTIONS.map((fixedQ) => {
    const correctIndex = Math.floor(Math.random() * 4);
    const choices: OverlayShapeChoiceData[] = fixedQ.distractors.map((d) => ({ pattern: d }));
    choices.splice(correctIndex, 0, { pattern: fixedQ.answer });
    return {
      questionData: {
        sheetA: fixedQ.sheetA,
        sheetB: fixedQ.sheetB,
      },
      choices,
      correctIndex,
      instructionText: INSTRUCTION_TEXT,
    };
  });
}

/** 正解判定関数 */
export function checkOverlayShapeAnswer(
  question: Question<OverlayShapeQuestionData, OverlayShapeChoiceData>,
  selectedIndex: number,
): boolean {
  return selectedIndex === question.correctIndex;
}
